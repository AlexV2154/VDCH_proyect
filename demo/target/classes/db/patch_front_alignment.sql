ALTER TABLE clientes
    ADD COLUMN IF NOT EXISTS notas TEXT;

ALTER TABLE productos
    ADD COLUMN IF NOT EXISTS tipo_venta VARCHAR(30) NOT NULL DEFAULT 'UNIDAD',
    ADD COLUMN IF NOT EXISTS unidad_base VARCHAR(30) NOT NULL DEFAULT 'UNIDAD',
    ADD COLUMN IF NOT EXISTS equivalencia NUMERIC(12, 3) NOT NULL DEFAULT 1;

ALTER TABLE detalle_ventas
    ADD COLUMN IF NOT EXISTS unidad_venta VARCHAR(30) NOT NULL DEFAULT 'UNIDAD',
    ADD COLUMN IF NOT EXISTS cantidad_texto VARCHAR(60);

ALTER TABLE abastecimientos
    ADD COLUMN IF NOT EXISTS tipo_abastecimiento VARCHAR(60) NOT NULL DEFAULT 'PROVEEDOR',
    ADD COLUMN IF NOT EXISTS contacto VARCHAR(80),
    ADD COLUMN IF NOT EXISTS comprobante VARCHAR(80);

ALTER TABLE detalle_abastecimientos
    ADD COLUMN IF NOT EXISTS unidad_compra VARCHAR(30) NOT NULL DEFAULT 'UNIDAD',
    ADD COLUMN IF NOT EXISTS precio_venta NUMERIC(12, 2) NOT NULL DEFAULT 0;
