CREATE TABLE IF NOT EXISTS usuarios (
    id_usuario BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(120) NOT NULL,
    usuario VARCHAR(60) NOT NULL UNIQUE,
    clave VARCHAR(255) NOT NULL,
    rol VARCHAR(40) NOT NULL DEFAULT 'ADMIN',
    estado BOOLEAN NOT NULL DEFAULT TRUE,
    fecha_creacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS clientes (
    id_cliente BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombres VARCHAR(150) NOT NULL,
    telefono VARCHAR(30),
    direccion VARCHAR(200),
    notas TEXT,
    estado BOOLEAN NOT NULL DEFAULT TRUE,
    fecha_registro TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS categorias (
    id_categoria BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(120) NOT NULL UNIQUE,
    descripcion TEXT,
    estado BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE IF NOT EXISTS productos (
    id_producto BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_categoria BIGINT REFERENCES categorias(id_categoria),
    nombre VARCHAR(150) NOT NULL,
    descripcion TEXT,
    tipo_venta VARCHAR(30) NOT NULL DEFAULT 'UNIDAD',
    unidad_base VARCHAR(30) NOT NULL DEFAULT 'UNIDAD',
    equivalencia NUMERIC(12, 3) NOT NULL DEFAULT 1,
    precio_compra NUMERIC(12, 2) NOT NULL DEFAULT 0,
    precio_venta NUMERIC(12, 2) NOT NULL DEFAULT 0,
    stock_actual NUMERIC(12, 3) NOT NULL DEFAULT 0,
    stock_minimo NUMERIC(12, 3) NOT NULL DEFAULT 0,
    lote VARCHAR(80),
    fecha_vencimiento DATE,
    estado BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT chk_productos_tipo_venta CHECK (tipo_venta IN ('UNIDAD', 'GRANEL', 'PAQUETE'))
);

CREATE TABLE IF NOT EXISTS ventas (
    id_venta BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_cliente BIGINT REFERENCES clientes(id_cliente),
    id_usuario BIGINT REFERENCES usuarios(id_usuario),
    canal_venta VARCHAR(40) NOT NULL DEFAULT 'TIENDA',
    tipo_pago VARCHAR(30) NOT NULL DEFAULT 'CONTADO',
    total NUMERIC(12, 2) NOT NULL DEFAULT 0,
    monto_pagado NUMERIC(12, 2) NOT NULL DEFAULT 0,
    saldo_pendiente NUMERIC(12, 2) NOT NULL DEFAULT 0,
    estado VARCHAR(30) NOT NULL DEFAULT 'REGISTRADA',
    fecha_venta TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_ventas_tipo_pago CHECK (tipo_pago IN ('CONTADO', 'FIADO', 'MIXTO')),
    CONSTRAINT chk_ventas_estado CHECK (estado IN ('REGISTRADA', 'ANULADA', 'PAGADA', 'PENDIENTE'))
);

CREATE TABLE IF NOT EXISTS detalle_ventas (
    id_detalle_venta BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_venta BIGINT NOT NULL REFERENCES ventas(id_venta) ON DELETE CASCADE,
    id_producto BIGINT NOT NULL REFERENCES productos(id_producto),
    cantidad NUMERIC(12, 3) NOT NULL,
    unidad_venta VARCHAR(30) NOT NULL DEFAULT 'UNIDAD',
    cantidad_texto VARCHAR(60),
    precio_unitario NUMERIC(12, 2) NOT NULL,
    subtotal NUMERIC(12, 2) NOT NULL
);

CREATE TABLE IF NOT EXISTS creditos (
    id_credito BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_venta BIGINT NOT NULL REFERENCES ventas(id_venta),
    id_cliente BIGINT NOT NULL REFERENCES clientes(id_cliente),
    monto_total NUMERIC(12, 2) NOT NULL,
    saldo_pendiente NUMERIC(12, 2) NOT NULL,
    estado VARCHAR(30) NOT NULL DEFAULT 'PENDIENTE',
    fecha_credito TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    fecha_limite DATE,
    observacion TEXT,
    CONSTRAINT chk_creditos_estado CHECK (estado IN ('PENDIENTE', 'PAGADO', 'VENCIDO', 'ANULADO'))
);

CREATE TABLE IF NOT EXISTS pagos_credito (
    id_pago_credito BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_credito BIGINT NOT NULL REFERENCES creditos(id_credito) ON DELETE CASCADE,
    monto_pagado NUMERIC(12, 2) NOT NULL,
    metodo_pago VARCHAR(40) NOT NULL DEFAULT 'EFECTIVO',
    fecha_pago TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    observacion TEXT
);

CREATE TABLE IF NOT EXISTS abastecimientos (
    id_abastecimiento BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_usuario BIGINT REFERENCES usuarios(id_usuario),
    proveedor VARCHAR(150),
    lugar_compra VARCHAR(150),
    tipo_abastecimiento VARCHAR(60) NOT NULL DEFAULT 'PROVEEDOR',
    contacto VARCHAR(80),
    comprobante VARCHAR(80),
    total NUMERIC(12, 2) NOT NULL DEFAULT 0,
    fecha_abastecimiento TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    observacion TEXT
);

CREATE TABLE IF NOT EXISTS detalle_abastecimientos (
    id_detalle_abastecimiento BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_abastecimiento BIGINT NOT NULL REFERENCES abastecimientos(id_abastecimiento) ON DELETE CASCADE,
    id_producto BIGINT NOT NULL REFERENCES productos(id_producto),
    cantidad NUMERIC(12, 3) NOT NULL,
    unidad_compra VARCHAR(30) NOT NULL DEFAULT 'UNIDAD',
    costo_unitario NUMERIC(12, 2) NOT NULL,
    precio_venta NUMERIC(12, 2) NOT NULL DEFAULT 0,
    lote VARCHAR(80),
    fecha_vencimiento DATE,
    subtotal NUMERIC(12, 2) NOT NULL
);

CREATE TABLE IF NOT EXISTS egresos (
    id_egreso BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_usuario BIGINT REFERENCES usuarios(id_usuario),
    tipo_egreso VARCHAR(80) NOT NULL,
    descripcion TEXT,
    monto NUMERIC(12, 2) NOT NULL,
    fecha_egreso TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_productos_nombre ON productos(nombre);
CREATE INDEX IF NOT EXISTS idx_clientes_nombres ON clientes(nombres);
CREATE INDEX IF NOT EXISTS idx_ventas_fecha ON ventas(fecha_venta);
CREATE INDEX IF NOT EXISTS idx_creditos_estado ON creditos(estado);
CREATE INDEX IF NOT EXISTS idx_abastecimientos_fecha ON abastecimientos(fecha_abastecimiento);
